    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String nombre = Nombre.getText();
        int cedula = Integer.parseInt(Cedula.getText());
        String apellido = Apellido.getText();
        String vivienda = Vivienda.getText();
        String correo = Correo.getText();
        String Pass = pass.getText();
        
        Usuario newUsuario = new Usuario(correo,cedula,nombre,apellido,vivienda,Pass);
        FuncionesUsuario funcion = new FuncionesUsuario();
        
        funcion.insertar(newUsuario);        
        
        Login L = new Login();
        L.setVisible(true);
        this.setVisible(false);
        
    }//GEN-LAST:event_jButton1ActionPerformed

